import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => {
  // Check if dark mode is active
  const isDarkMode = document.documentElement.classList.contains('dark');
  
  return (
    <div
      ref={ref}
      className={cn(
        "rounded-lg border border-gray-300 bg-white dark:border-gray-600 dark:bg-gray-800 text-gray-900 dark:text-gray-100 shadow-sm",
        className
      )}
      style={{
        backgroundColor: isDarkMode ? 'rgb(31, 41, 55)' : 'white',
        color: isDarkMode ? 'rgb(243, 244, 246)' : 'rgb(17, 24, 39)',
        border: isDarkMode ? '1px solid rgb(75, 85, 99)' : '1px solid rgb(209, 213, 219)',
        ...props.style
      }}
      {...props}
    />
  )
})
Card.displayName = "Card"

